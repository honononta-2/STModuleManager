/** カスタム言語JSONをJAをベースにマージ（不足キーは日本語で補完） */
export function mergeLang(custom: unknown): LangData {
  const c = custom as Partial<LangData>;
  return {
    stat_names: { ...JA.stat_names, ...(c.stat_names ?? {}) },
    module_types: { ...JA.module_types, ...(c.module_types ?? {}) },
    rarity: { ...JA.rarity, ...(c.rarity ?? {}) },
    ui: { ...JA.ui, ...(c.ui ?? {}) },
  };
}

/** テンプレート文字列の変数を置換 ({count}, {name} 等) */
export function fmt(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
}
