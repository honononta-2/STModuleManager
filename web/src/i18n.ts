export let t: LangData = JA;

const LANGS: Record<string, LangData> = { ja: JA, ko: KO, en: EN };
const LANG_STORAGE_KEY = "lang";

export function setLang(lang: LangData): void {
  t = lang;
}

export function statName(partId: number): string {
  return t.stat_names[String(partId)] ?? `Unknown(${partId})`;
}

export function fmt(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
}

export function applyI18n(): void {
  document.querySelectorAll<HTMLElement>("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n!;
    const val = t.ui[key];
    if (val != null) el.textContent = val;
  });
  document.querySelectorAll<HTMLElement>("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder!;
    const val = t.ui[key];
    if (val != null) (el as HTMLInputElement).placeholder = val;
  });
}

export function getSavedLang(): string {
  return localStorage.getItem(LANG_STORAGE_KEY) ?? "ja";
}

export function initLang(): void {
  const saved = getSavedLang();
  setLang(LANGS[saved] ?? JA);
}

export function saveLang(code: string): void {
  localStorage.setItem(LANG_STORAGE_KEY, code);
  setLang(LANGS[code] ?? JA);
}

/** 旧フォーマット（日本語名）のステータス配列をpart_idに変換 */
export function migrateStatNamesToIds(arr: (string | number)[]): number[] {
  const jaNameToId: Record<string, number> = {};
  for (const [id, name] of Object.entries(JA.stat_names)) {
    jaNameToId[name] = Number(id);
  }
  return arr
    .map((item) => (typeof item === "number" ? item : jaNameToId[item] ?? 0))
    .filter((id) => id > 0);
}
